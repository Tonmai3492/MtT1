# รับค่าน้ำหนัก (กิโลกรัม)
weight = float(input("กรุณาใส่น้ำหนัก (กก.): "))

# รับค่าส่วนสูง (เซนติเมตร)
height_cm = float(input("กรุณาใส่ส่วนสูง (ซม.): "))

# แปลงส่วนสูงเป็นเมตร
height_m = height_cm / 100

# คำนวณ BMI
bmi = weight / (height_m ** 2)

# แสดงผล
print(f"\nค่า BMI ของคุณคือ: {bmi:.2f}")

# แปลผล BMI
if bmi < 18.5:
    print("อยู่ในเกณฑ์น้ำหนักน้อย")
elif bmi < 25:
    print("อยู่ในเกณฑ์น้ำหนักปกติ")
elif bmi < 30:
    print("อยู่ในเกณฑ์น้ำหนักเกิน")
else:
    print("อยู่ในเกณฑ์โรคอ้วน")